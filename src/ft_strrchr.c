char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*p;

	p = 0;
	i = -1;
	while (s[++i])
		if (s[i] == c)
			p = (char *)&s[i];
	if (s[i] == c)
		p = (char *)&s[i];
	return (p);
}
